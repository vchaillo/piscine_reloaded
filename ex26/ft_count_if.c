int			ft_count_if(char **tab, int (*f)(char *))
{
	int		ret;
	int		i;

	ret = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			ret++;
		i++;
	}
	return (ret);
}
